# Число 5 в двочном виде 00000101
# При сдвиге в право получаем 00000001
print('Сдвиг 5 в право на 2 бита', 5 >> 2)
# При сдвиге в право получаем 00010100
print('Сдвиг 5 в лево на 2 бита', 5 << 2)

# Число 6 в двочном виде 00000110
# При сдвиге в право получаем 00000001
print('Сдвиг 6 в право на 2 бита', 6 >> 2)
# При сдвиге в право получаем 00011000
